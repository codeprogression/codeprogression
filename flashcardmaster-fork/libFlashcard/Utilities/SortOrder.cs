namespace LibFlashcard.Utilities
{
    // Summary:
    //     Specifies how items in a list are sorted.
    public enum SortOrder
    {
        // Summary:
        //     The items are not sorted.
        None = 0,
        //
        // Summary:
        //     The items are sorted in ascending order.
        Ascending = 1,
        //
        // Summary:
        //     The items are sorted in descending order.
        Descending = 2,
    }
}
